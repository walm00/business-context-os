class SchemaVersionError(Exception):
    """Raised by strict consumers when an artifact's version is unsupported."""


@dataclass(frozen=True)
class SchemaWarning:
    artifact_id: str
    found: str | None
    expected_current: str
    min_supported: str
    detail: str

    def as_finding(self) -> dict:
        return {
            "type": "schema-version-mismatch",
            "severity": "amber",
            "artifact": self.artifact_id,
            "found": self.found,
            "expected_current": self.expected_current,
            "min_supported": self.min_supported,
            "detail": self.detail,
        }


def validate_schema(
    artifact_id: str,
    found_version: str | None,
    *,
    path: Path | None = None,
) -> SchemaWarning | None:
    """Validate an artifact's schema version against the registry.

    Strict policy → raises `SchemaVersionError` on unknown / pre-min versions.
    Warn policy   → returns a `SchemaWarning` for the caller to surface
                   (or None if the version is current).

    Unknown `artifact_id` → raises `KeyError` so typos fail loudly during
    development rather than silently skipping validation.
    """
    schema = REGISTRY[artifact_id]
    if found_version is None:
        msg = (
            f"{artifact_id}: missing schema_version; expected {schema.current!r} "
            f"(min supported: {schema.min_supported!r})"
        )
        if schema.policy == "strict":
            raise SchemaVersionError(f"{path or artifact_id}: {msg}")
        return SchemaWarning(artifact_id, None, schema.current, schema.min_supported, msg)

    found_str = str(found_version)
    if found_str == schema.current:
        return None

    if _is_below_min(found_str, schema.min_supported):
        msg = (
            f"{artifact_id}: schema_version {found_str!r} is older than "
            f"min_supported {schema.min_supported!r}; run update.py to migrate"
        )
        if schema.policy == "strict":
            raise SchemaVersionError(f"{path or artifact_id}: {msg}")
        return SchemaWarning(artifact_id, found_str, schema.current, schema.min_supported, msg)

    # Older but still supported, or newer (forward-compat). Warn either way.
    direction = "older but still supported" if _lt(found_str, schema.current) else "newer than this build"
    return SchemaWarning(
        artifact_id,
        found_str,
        schema.current,
        schema.min_supported,
        f"{artifact_id}: schema_version {found_str!r} is {direction} (current {schema.current!r})",
    )


def _normalize(v: str) -> tuple[int, ...]:
    """Best-effort version-tuple. Pre-release suffixes sort below their base."""
    base = v.split("-", 1)[0]
    parts = []
    for piece in base.split("."):
        try:
            parts.append(int(piece))
        except ValueError:
            parts.append(0)
    return tuple(parts)


def _lt(a: str, b: str) -> bool:
    return _normalize(a) < _normalize(b)


def _is_below_min(found: str, min_supported: str) -> bool:
    return _lt(found, min_supported)
