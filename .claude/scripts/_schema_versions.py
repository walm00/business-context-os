"""Central schema-version registry for BCOS derived artifacts.

Why this exists
---------------
Before this module, every JSON/JSONL artifact in the framework had its own
ad-hoc version handling — `digest_sidecar` raised on unknown versions
(hard-crashed the dashboard); `wiki_schema` warned only; `promote_resolutions`
had a SCHEMA_VERSION constant but never validated reads; `record_resolution`
didn't version at all. Five different policies for "what does this artifact's
shape mean and what happens on upgrade?".

This module is the single source of truth: every artifact gets one row in
`REGISTRY`, every consumer calls `validate_schema()` instead of rolling its
own check.

Migration policy
----------------
- `current`     — the version this build of the framework writes.
- `min_supported` — the oldest version this build can still READ.
                   Versions older than this raise on read; consumers should
                   prompt the user to run `update.py` to migrate.
- `policy`      — `"strict"` raises on unknown/unsupported,
                  `"warn"` logs and continues with best-effort parse.

Strict for runtime-critical artifacts (dispatcher sidecar, learning ladder).
Warn for advisory artifacts (wiki, lessons, inventory snapshots).

Usage
-----
    from _schema_versions import validate_schema

    data = json.loads(path.read_text())
    validate_schema("digest-sidecar", data.get("schema_version"), path=path)
    # raises SchemaVersionError on incompatibility (strict)
    # or returns a SchemaWarning instance (warn) for the caller to surface
"""

from __future__ import annotations

from dataclasses import dataclass
from pathlib import Path
from typing import Literal


Policy = Literal["strict", "warn"]


@dataclass(frozen=True)
class ArtifactSchema:
    artifact_id: str
    current: str
    min_supported: str
    policy: Policy
    notes: str = ""


